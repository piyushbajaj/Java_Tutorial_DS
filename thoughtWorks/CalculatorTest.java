package thoughtWorks;

import org.junit.Assert;
import org.junit.Test;

/**
 * Unit test for @{@link Calculator}
 * Tests have been written against add/subtract for a calculator class.
 * All you have to do is to complete the implementations of the methods
 *
 * Multiplication has been implemented as an example.
 */
public class CalculatorTest

{
    @Test
    public void shouldAddTwoNumbers(){
        Calculator calculator = new Calculator();
        Assert.assertEquals(3,calculator.add(1,2));
    }

    @Test
    public void shouldSubtractTwoNumbers(){
        Calculator calculator = new Calculator();
        Assert.assertEquals(1,calculator.subtract(2,1));
    }


    @Test
    public void shouldReturnPositiveNumberWhenPositiveNumbersAreMultiplied(){
        Calculator calculator = new Calculator();
        Assert.assertEquals(8,calculator.multiply(4,2));
    }
}
